#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int			idx;
	int			idx_join;
	char		*join;
	int			area;

	area = ft_strlen((char *)s1) + ft_strlen((char *)s2) + 1;
	join = malloc(sizeof(char) * (area));
	if (!s1 || !s2 || !(join))
		return (0);
	idx = 0;
	idx_join = 0;
	while (s1[idx])
		join[idx_join++] = s1[idx++];
	idx = 0;
	while (s2[idx])
		join[idx_join++] = s2[idx++];
	join[idx_join] = '\0';
	return (join);
}
